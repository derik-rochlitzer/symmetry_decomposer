from math import pi, cos, sqrt


def _cos(x):
    """
    Compute the cosine value of a given angle in degrees.

    Parameters
    ----------
    x: float
        Angle in degrees
    
    Returns
    -------
    float
        Cosine value of x
    """
    x = x * pi / 180
    return cos(x)


